package com.github.m0nk3y2k4.thetvdb.testutils.parameterized;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import org.junit.jupiter.params.provider.ArgumentsSource;

/**
 * Custom test data source annotation for {@link EmptyStringProvider}
 * <p><br>
 * Serves as an ArgumentsSource providing multiple empty String values to the annotated @ParameterizedTest method.
 */
@Documented
@Target({ElementType.ANNOTATION_TYPE, ElementType.METHOD})
@Retention(RetentionPolicy.RUNTIME)
@ArgumentsSource(EmptyStringProvider.class)
public @interface EmptyStringSource {
}
